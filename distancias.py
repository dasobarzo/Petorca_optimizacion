# Crear una lista para almacenar las distancias
distancias = []

# Calcular distancias entre cada par de puntos
for i in range(1, len(puntos)):
    for j in range(i + 1, len(puntos) + 1):
        lat1, lon1 = coordenadas_decimales[i]
        lat2, lon2 = coordenadas_decimales[j]
        distancia = haversine(lat1, lon1, lat2, lon2)
        distancias.append((f"Distancia entre el punto {i} y el punto {j}", distancia))

# Mostrar resultados
for resultado in distancias:
    print(f"{resultado[0]}: {resultado[1]:.2f} km")
