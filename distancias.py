import math

# Función para convertir coordenadas en formato grados-minutos-segundos a decimal
def dms_a_decimal(grados, minutos, segundos, direccion):
    decimal = grados + minutos/60 + segundos/3600
    if direccion in ['S', 'W']:  # Si es sur o oeste, debe ser negativo
        decimal *= -1
    return decimal

# Función para calcular la distancia con la fórmula de Haversine
def haversine(lat1, lon1, lat2, lon2):
    R = 6371  # Radio de la Tierra en km
    # Convertir las latitudes y longitudes de grados a radianes
    lat1, lon1, lat2, lon2 = map(math.radians, [lat1, lon1, lat2, lon2])
    
    # Diferencias
    dlat = lat2 - lat1
    dlon = lon2 - lon1
    
    # Aplicar fórmula de Haversine
    a = math.sin(dlat/2)**2 + math.cos(lat1) * math.cos(lat2) * math.sin(dlon/2)**2
    c = 2 * math.asin(math.sqrt(a))
    
    # Distancia
    distancia = R * c
    return distancia

# Función para convertir las coordenadas de formato DMS a decimal
def coordenadas_a_decimales(lat_dms, lon_dms):
    # Descomposición del formato DMS (Grados, Minutos, Segundos)
    lat_grados, lat_minutos, lat_segundos, lat_direccion = int(lat_dms[:2]), int(lat_dms[3:5]), int(lat_dms[6:8]), lat_dms[-1]
    lon_grados, lon_minutos, lon_segundos, lon_direccion = int(lon_dms[:2]), int(lon_dms[3:5]), int(lon_dms[6:8]), lon_dms[-1]
    
    lat_decimal = dms_a_decimal(lat_grados, lat_minutos, lat_segundos, lat_direccion)
    lon_decimal = dms_a_decimal(lon_grados, lon_minutos, lon_segundos, lon_direccion)
    
    return lat_decimal, lon_decimal

